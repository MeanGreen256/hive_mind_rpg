def _pulse_frame(base: list[str], path: list[tuple[int, int]], window_index: int) -> list[str]:
    grid = [list(row) for row in base]
    quarter = max(len(path) // 4, 1)
    start = window_index * quarter
    lit = path[start:start + quarter + 2]
    for x, y in lit:
        if grid[y][x] in ("q", "r", "p"):
            grid[y][x] = "t"
    return ["".join(row) for row in grid]


def shimmer_loop_a() -> list[list[str]]:
    return [_pulse_frame(VEIN_DIAGONAL, VEIN_DIAGONAL_PATH, i) for i in range(4)]


def shimmer_loop_b() -> list[list[str]]:
    # Whole-vein breathing pulse: dim, mid, bright, mid.
    dim = remap(VEIN_BRANCH, {"r": "q", "q": "p"})
    mid = VEIN_BRANCH
    bright = remap(VEIN_BRANCH, {"q": "r", "r": "t"})
    return [dim, mid, bright, mid]


