def parse(text: str) -> list[str]:
    rows = [line for line in text.strip("\n").split("\n")]
    width = len(rows[0])
    for row in rows:
        assert len(row) == width, f"ragged row: {row!r} ({len(row)} != {width})"
    return rows


def hflip(rows: list[str]) -> list[str]:
    return [row[::-1] for row in rows]


def vflip(rows: list[str]) -> list[str]:
    return rows[::-1]


def rot90(rows: list[str]) -> list[str]:
    # Clockwise quarter turn; legal for tiles/FX per bible section 5.
    return ["".join(rows[len(rows) - 1 - x][y] for x in range(len(rows))) for y in range(len(rows[0]))]


def shift_up(rows: list[str], amount: int = 1) -> list[str]:
    blank = "." * len(rows[0])
    return rows[amount:] + [blank] * amount


def shift_down(rows: list[str], amount: int = 1) -> list[str]:
    blank = "." * len(rows[0])
    return [blank] * amount + rows[:-amount]


def remap(rows: list[str], table: dict[str, str]) -> list[str]:
    return ["".join(table.get(ch, ch) for ch in row) for row in rows]


def overlay(base: list[str], top: list[str]) -> list[str]:
    return [
        "".join(t if t != "." else b for b, t in zip(base_row, top_row))
        for base_row, top_row in zip(base, top)
    ]


def blit(image: Image.Image, rows: list[str], palette: dict[str, tuple], x0: int, y0: int) -> None:
    for y, row in enumerate(rows):
        for x, ch in enumerate(row):
            color = palette[ch]
            if color is not None:
                image.putpixel((x0 + x, y0 + y), color)


