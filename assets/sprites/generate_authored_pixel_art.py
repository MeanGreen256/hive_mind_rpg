def zone1_tiles() -> dict[tuple[int, int], list[str]]:
    edge_base = WALL_CANOPY_A

    def edge(rim: list[str]) -> list[str]:
        return overlay(edge_base, rim)

    rim_n = RIM_N
    rim_e = rot90(rim_n)
    rim_s = rot90(rim_e)
    rim_w = rot90(rim_s)
    inner_ne = RIM_INNER_NE
    inner_se = rot90(inner_ne)
    inner_sw = rot90(inner_se)
    inner_nw = rot90(inner_sw)

    tiles: dict[tuple[int, int], list[str]] = {}
    # Row 0 — floor variants (6).
    floors = [
        FLOOR_MOSS_PLAIN,
        FLOOR_MOSS_TUFTS,
        FLOOR_SOIL_PATCH,
        FLOOR_ROOT,
        hflip(FLOOR_MOSS_TUFTS),
        vflip(FLOOR_SOIL_PATCH),
    ]
    for index, rows in enumerate(floors):
        tiles[(index, 0)] = rows
    # Row 1 — wall variants (8).
    walls = [
        WALL_CANOPY_A,
        WALL_CANOPY_B,
        WALL_TRUNK,
        WALL_STONE,
        WALL_ROOTS,
        hflip(WALL_TRUNK),
        hflip(WALL_STONE),
        hflip(WALL_ROOTS),
    ]
    for index, rows in enumerate(walls):
        tiles[(index, 1)] = rows
    # Row 2 — edges (4) + outer corners (4): rims toward the adjacent floor.
    tiles[(0, 2)] = edge(rim_n)
    tiles[(1, 2)] = edge(rim_e)
    tiles[(2, 2)] = edge(rim_s)
    tiles[(3, 2)] = edge(rim_w)
    tiles[(4, 2)] = edge(overlay(rim_n, rim_e))  # floor N+E
    tiles[(5, 2)] = edge(overlay(rim_s, rim_e))  # floor S+E
    tiles[(6, 2)] = edge(overlay(rim_s, rim_w))  # floor S+W
    tiles[(7, 2)] = edge(overlay(rim_n, rim_w))  # floor N+W
    # Row 3 — inner corners (4) + static corruption veins (4).
    tiles[(0, 3)] = edge(inner_ne)
    tiles[(1, 3)] = edge(inner_se)
    tiles[(2, 3)] = edge(inner_sw)
    tiles[(3, 3)] = edge(inner_nw)
    tiles[(4, 3)] = VEIN_DIAGONAL
    tiles[(5, 3)] = VEIN_BRANCH
    tiles[(6, 3)] = rot90(VEIN_DIAGONAL)
    tiles[(7, 3)] = hflip(VEIN_BRANCH)
    # Row 4 — two 4-frame shimmer loops.
    for index, rows in enumerate(shimmer_loop_a()):
        tiles[(index, 4)] = rows
    for index, rows in enumerate(shimmer_loop_b()):
        tiles[(4 + index, 4)] = rows
    return tiles


def build_zone1_atlas() -> Image.Image:
    atlas = Image.new("RGBA", (ATLAS_COLUMNS * TILE, ATLAS_ROWS * TILE), (0, 0, 0, 0))
    for (column, row), rows in zone1_tiles().items():
        blit(atlas, rows, TILE_PALETTE, column * TILE, row * TILE)
    return atlas


