def build_chaser_sheet() -> tuple[Image.Image, dict[str, dict]]:
    animations = chaser_animations()
    sheet = Image.new(
        "RGBA",
        (CHASER_SHEET_COLUMNS * CHASER_FRAME, len(ANIMATION_ROW_ORDER) * CHASER_FRAME),
        (0, 0, 0, 0),
    )
    for row, name in enumerate(ANIMATION_ROW_ORDER):
        for column, frame in enumerate(animations[name]["frames"]):
            logical_frame = Image.new(
                "RGBA", (CHASER_LOGICAL_FRAME, CHASER_LOGICAL_FRAME), (0, 0, 0, 0)
            )
            blit(logical_frame, frame, CHASER_PALETTE, 0, 0)
            display_frame = logical_frame.resize(
                (CHASER_FRAME, CHASER_FRAME), Image.Resampling.NEAREST
            )
            sheet.alpha_composite(display_frame, (column * CHASER_FRAME, row * CHASER_FRAME))
    return sheet, animations


def write_sprite_frames_tres(path: str, animations: dict[str, dict]) -> None:
    subresources: list[str] = []
    animation_blocks: list[str] = []
    for row, name in enumerate(ANIMATION_ROW_ORDER):
        animation = animations[name]
        frame_entries: list[str] = []
        for column in range(len(animation["frames"])):
            atlas_id = f"AtlasTexture_{name}_{column}"
            subresources.append(
                f'[sub_resource type="AtlasTexture" id="{atlas_id}"]\n'
                f'atlas = ExtResource("1_sheet")\n'
                f"region = Rect2({column * CHASER_FRAME}, {row * CHASER_FRAME}, {CHASER_FRAME}, {CHASER_FRAME})\n"
            )
            frame_entries.append(
                '{\n"duration": 1.0,\n"texture": SubResource("%s")\n}' % atlas_id
            )
        loop = "true" if animation["loop"] else "false"
        animation_blocks.append(
            '{\n"frames": [%s],\n"loop": %s,\n"name": &"%s",\n"speed": %.1f\n}'
            % (", ".join(frame_entries), loop, name, animation["speed"])
        )
    load_steps = len(subresources) + 2
    content = (
        f'[gd_resource type="SpriteFrames" load_steps={load_steps} format=3]\n\n'
        '[ext_resource type="Texture2D" path="res://assets/sprites/enemies/melee_chaser.png" id="1_sheet"]\n\n'
        + "\n".join(subresources)
        + "\n[resource]\nanimations = ["
        + ", ".join(animation_blocks)
        + "]\n"
    )
    with open(path, "w", encoding="utf-8") as handle:
        handle.write(content)


