def keyed_windup(frame_index: int) -> list[str]:
    # Progressive telegraph keying: horn tips, then highlights, then the
    # carapace itself shift to the #FFC72E signal color.
    base = CHASER_WINDUP_BASE
    if frame_index == 0:
        return remap(base, {"l": "Y"})
    if frame_index == 1:
        rows = remap(base, {"l": "Y"})
        # Flood the upper carapace rows with the key color.
        keyed = []
        for y, row in enumerate(rows):
            if 9 <= y <= 12:
                row = row.replace("m", "Y")
            keyed.append(row)
        return keyed
    return remap(base, {"l": "Y", "m": "Y"})


